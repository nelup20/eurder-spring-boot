package com.neluplatonov.eurder.validator;

import java.util.List;

public class CustomerValidator {
    private static final String EMAIL_REGEX_PATTERN = "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])";

    public static void validateEmail(String emailToValidate){
        if(!emailToValidate.matches(EMAIL_REGEX_PATTERN)) throw new IllegalArgumentException("The email address provided is not valid! Make sure your email address looks like this: john@doe.com");
    }

    public static void validateArgumentsAreNotEmpty(List<String> argumentsToValidate){
        if(argumentsToValidate.stream().anyMatch(argument -> argument.length() == 0)) throw new IllegalArgumentException("None of the fields for a new customer's data may be empty");
    }
}
